package OOP.Inheritance.Compare;

public class ThreePoint {
    private int x;
    private int y;
    private int z;

    public ThreePoint(int y, int x, int z) {
        this.y = y;
        this.x = x;
        this.z = z;
    }

    @Override
    public boolean equals(Object obj) {
        return super.equals(obj);
    }
}
